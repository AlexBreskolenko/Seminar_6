//Метод вывода массива с последущей заменой элементов
void PrintArray(int[,] arr)
{
    for (int col = 0; col < arr.GetLength(0); col++)
    {
        for (int rows = 0; rows < arr.GetLength(1); rows++)
        {
            if (arr[col, rows] == 0)
            {
                Console.Write($" ");
            }
            else
            {
                Console.Write($"+");
            }
        }
        Console.WriteLine();
    }
}

//Метод закрашивания 
void FillImage(int[,] pic,int rows, int col)
{
    //если элемент незакрашен
    if(pic[rows, col] == 0)
    {
        //То закрашиваем его
        picture[rows, col] = 1;
        //Вызываем еще раз функцию (делаем рекурсию)
        FillImage(pic, rows - 1, col);
        FillImage(pic, rows, col - 1);
        FillImage(pic, rows + 1, col);
        FillImage(pic, rows, col + 1);
    }
}

